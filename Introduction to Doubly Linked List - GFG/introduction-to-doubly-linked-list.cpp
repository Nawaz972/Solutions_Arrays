class Solution {
  public:
    Node* constructDLL(vector<int>& arr) {
        // code here
        Node* head = new Node(arr[0]);
        Node* temp = head;
        int n = arr.size();
        int cnt = 1;
        while(cnt < n)
        {
            Node* newNode = new Node(arr[cnt]);
            newNode -> prev = temp;
            temp -> next = newNode;
            temp = temp -> next;
            cnt++;
        }
        return head;
    }
};

